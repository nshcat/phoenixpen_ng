package com.phoenixpen.game.simulation

/**
 * An enumeration describing the different types of eternal season that are supported
 */
enum class EternalSeasonType
{
    /**
     * Spring, with green leaves
     */
    Spring,

    /**
     * Spring, trees in bloom
     */
    SpringBloom,

    /**
     * Summer, green leaves
     */
    Summer,

    /**
     * Summer, with fruit on trees
     */
    SummerFruit,

    /**
     * Autumn, with brown leaves
     */
    Autumn,

    /**
     * Autumn, with dropped leaves
     */
    AutumnDropped,

    /**
     * Winter, with snow
     */
    Winter
}